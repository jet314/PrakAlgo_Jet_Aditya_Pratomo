def calculate_average(tuple_of_tuples):
    # Menghitung jumlah kolom dan baris dalam tuple
    num_rows = len(tuple_of_tuples)
    num_cols = len(tuple_of_tuples[0])

    # Inisialisasi list untuk menyimpan total setiap kolom
    column_sums = [0] * num_cols

    # Iterasi melalui setiap kolom dan baris, mengakumulasikan nilai
    for row in tuple_of_tuples:
        for col_index, value in enumerate(row[0].split(',')):
            cleaned_value = float(value.replace('*', ''))  # Menghilangkan karakter '*'
            column_sums[col_index] += cleaned_value

    # Menghitung rata-rata untuk setiap kolom
    column_averages = [round(sum_values / num_rows, 1) for sum_values in column_sums]

    return column_averages

