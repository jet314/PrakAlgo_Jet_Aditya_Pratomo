def count_matching_strings(input_list):
    matching_strings = [s for s in input_list if isinstance(s, str) and len(s) >= 2 and s[0] == s[-1]]
    return matching_strings

# Contoh list
example_list = ['z', 'xvc', 'cac', 244]

